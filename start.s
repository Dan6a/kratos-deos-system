// Kratos OS — bare-metal точка входа
// RPi Zero 2W запускает 4 ядра. Ядра 1-3 паркуем, ядро 0 запускает Kratos.
//
// Последовательность:
//   1. Проверяем номер ядра (MPIDR_EL1)
//   2. Ядра 1-3 → WFE (ждут сигнала, Аксиома 8: не тратим ресурсы)
//   3. Ядро 0 → настраиваем стек, обнуляем BSS, прыгаем в Rust

.section .text.boot
.global _start

_start:
    // ── Паркуем ядра 1-3 ─────────────────────────────────────────────────
    mrs     x0, mpidr_el1       // читаем ID ядра
    and     x0, x0, #0xFF       // берём биты [7:0] — номер ядра
    cbz     x0, .core0          // если ядро 0 — продолжаем
.park:
    wfe                         // ждём события (Аксиома 8: экономия энергии)
    b       .park

.core0:
    // ── Настраиваем стек ─────────────────────────────────────────────────
    ldr     x0, =_stack_top
    mov     sp, x0

    // ── Обнуляем BSS ─────────────────────────────────────────────────────
    ldr     x0, =_bss_start
    ldr     x1, =_bss_end
    cmp     x0, x1
    beq     .bss_done
.bss_loop:
    str     xzr, [x0], #8      // пишем 0, инкрементируем
    cmp     x0, x1
    blt     .bss_loop
.bss_done:

    // ── Прыгаем в Rust ────────────────────────────────────────────────────
    bl      kratos_main

    // ── Если вернулись (не должны) — паркуем ─────────────────────────────
.halt:
    wfe
    b       .halt
